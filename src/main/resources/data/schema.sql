CREATE TABLE IF NOT EXISTS POKEMON (
    ID BIGINT NOT NULL,
    NAME VARCHAR(255) NOT NULL,
    IMAGEURL VARCHAR(255),
    REGION VARCHAR(255),
    DESCRIPTION VARCHAR(1000) NOT NULL,
    TYPES VARCHAR(100),
    ABILITIES VARCHAR(255),
    
    PRIMARY KEY(ID)
);

CREATE TABLE IF NOT EXISTS VISIT_COUNTER (
    ID BIGINT NOT NULL,
    IP VARCHAR(255),
    COUNT INTEGER,
    COOKIE VARCHAR(255),
    
    PRIMARY KEY(ID)
);

